public class Location {
    private int posX = 15;
    private int posY = 30;

    public int getPosX(){
        return posX;
    }

    public void setPosX(int setPosX){
        posX = setPosX;
    }

    public int getPosY(){
        return posY;
    }

    public void setPosY(int setPosY){
        posY = setPosY;
    }
}
