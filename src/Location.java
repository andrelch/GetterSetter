public class Location {
    //private variables seen only in this class
    private int posX = 15;
    private int posY = 30;

    //getter method posX
    public int getPosX(){
        return posX;
    }

    //setter method posX
    public void setPosX(int setPosX){
        posX = setPosX;
    }

    //getter method posY
    public int getPosY(){
        return posY;
    }

    //setter method posY
    public void setPosY(int setPosY){
        posY = setPosY;
    }
}
